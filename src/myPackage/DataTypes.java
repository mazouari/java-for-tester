package myPackage;

public class DataTypes {

	public static void main(String[] args) {

		//Primitive
		int a = 10;
		double d = 10.555;
		char c = 'A';
		float f = 10.43f;
		
		//Derived
		String s = "Welcome";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(f);
		System.out.println(s);
		
		
		
				

	}

}
